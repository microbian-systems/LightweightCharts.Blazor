namespace LightweightCharts.Blazor.Customization.Enums
{
	/// <summary>
	/// Enumeration representing the sign of a marker.<br/>
	/// <see href="https://tradingview.github.io/lightweight-charts/docs/api/enumerations/MarkerSign"/>
	/// </summary>
	public enum MarkerSign
	{
		/// <summary>
		/// Represents a negative change (-1)
		/// </summary>
		Negative = -1,

		/// <summary>
		/// Represents no change (0)
		/// </summary>
		Neutral = 0,

		/// <summary>
		/// Represents a positive change (1)
		/// </summary>
		Positive = 1
	}
}
