namespace LightweightCharts.Blazor.Customization.Enums
{
	/// <summary>
	/// Represents the possible line styles.<br/>
	/// <see href="https://tradingview.github.io/lightweight-charts/docs/api/enums/LineStyle"/>
	/// </summary>
	public enum LineStyle
	{
		/// <summary>
		/// A solid line.
		/// </summary>
		Solid,

		/// <summary>
		/// A dotted line.
		/// </summary>
		Dotted,

		/// <summary>
		/// A dashed line.
		/// </summary>
		Dashed,

		/// <summary>
		/// A dashed line with bigger dashes.
		/// </summary>
		LargeDashed,

		/// <summary>
		/// A dottled line with more space between dots.
		/// </summary>
		SparseDotted
	}
}
